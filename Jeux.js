class Jeux {
    constructor() {
        this.nom = nom;
        this.chemin = chemin;
        this.cover = cover;
    }
    set nom(val) {
        this.nom = val;
    }
    get nom() {
        return this.nom;
    }
    set chemin(val) {
        this.chemin = val;
    }
    get chemin() {
        return this.chemin;
    }
    set cover(val) {
        this.cover = val;
    }
    get cover() {
        return this.cover;
    }
}